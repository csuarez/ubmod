    About UBMoD - University at Buffalo Metrics on Demand
  </div>
  <br/>
  <p>UBMoD is an open source data warehouse and web portal for mining
  statistical data from resource managers
  (<a href="http://www.clusterresources.com/pages/products/torque-resource-manager.php">Torque</a>,
  <a href="http://www.openpbs.org/">OpenPBS</a> and
  <a href="http://gridscheduler.sourceforge.net/">Sun Grid Engine</a>
  currently supported) commonly found in high-performance computing
  environments. UBMoD presents resource utilization (CPU cycles consumed,
  total jobs, avg wait time, etc) for individual users, and groups.  The
  information can be presented in easy to understand charts, graphs, and
  tables, with the added ability to display finer levels of detail. The UBMoD
  frontend is written in PHP. It uses MySQL as the backend database and uses
  Perl for parsing resource manager accounting files. The web portal runs on
  the Apache HTTP server and can be viewed using any popular modern web
  browser that supports Javascript.
  </p>
